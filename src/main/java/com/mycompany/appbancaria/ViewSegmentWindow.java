     private void columnsNameGrid() {
          Segmento segment = new Segmento();
          ResultSet rs = segment.viewSegment();
          tm = new DefaultTableModel(columnas, 0);
          try {
               while (rs.next()) {
                    Object[] ob = {
                         rs.getInt("id_segmento"),
                         rs.getString("nombre_segmento"),
                         rs.getString("rango_segmento")
                    };
                    tm.addRow(ob);
                    SegmentTable.setModel(tm);
               }
          } catch (Exception e) {
               JOptionPane.showMessageDialog(null,"Error: "+ e);
          }
     }
