     private void confirmBtmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmBtmMouseClicked
          // TODO add your handling code here:
          try (Connection connect = DriverManager.getConnection(url, user, password)){
               
               PreparedStatement insert = connect.prepareStatement("INSERT INTO clientes("
                       + "nombre, apellido, fecha_Nacimiento, tipo_Cuenta, Segmento)"
                       + "VALUES (?,?,?,?,?)");
               insert.setString(1,nameTxt.getText());
               insert.setString(2,lastNameTxt.getText());
               insert.setString(3,bornDateTxt.getText());
               insert.setString(4,accountTxt.getText());
               insert.setString(5,segmentTxt.getText());
               insert.executeUpdate();
               JOptionPane.showMessageDialog(null,
                       "Cliente Agregado Exitosamente");
          } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "Error: "+e);
          }
          
          
          
     }//GEN-LAST:event_confirmBtmMouseClicked

