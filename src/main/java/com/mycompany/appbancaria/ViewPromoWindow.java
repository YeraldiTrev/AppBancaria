     private void columnsNameGrid() {
          Promocion promo = new Promocion();
          ResultSet rs = promo.viewPromo();
          tm = new DefaultTableModel(columnas, 0);
          try {
               while (rs.next()) {
                    Object[] ob = {
                         rs.getInt("id_promocion"),
                         rs.getString("nombre_promocion"),
                         rs.getString("vigencia"),
                         rs.getString("descripcion")
                    };
                    tm.addRow(ob);
                    promoTable.setModel(tm);
               }
          } catch (Exception e) {
               JOptionPane.showMessageDialog(null,"Error: "+ e);
          }
     }
